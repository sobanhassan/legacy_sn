                <p>Legacy a été fondée par Pape Latyr Gning, PDG.  Pape est né à Dakar, Sénégal Afrique de l'Ouest.  Il a d'abord commencé à jouer au football lorsqu'il était enfant, puis il a commencé à grandir très vite et est devenu l'un des plus grands parmi ses amis.  Ils ont commencé à se moquer de lui en disant qu'il était trop grand pour le football et qu'il s'adaptait mieux au basket.  Après cela, il a décidé de travailler très dur et de jouer avec son frère aîné qui;  était déjà basketteur.  Pape a commencé à travailler, à s'entraîner, à s'entraîner et à rejoindre des clubs peu de temps après.  Étonnamment, avec une courte période, il est devenu très bon dans ce domaine et a excellé énormément avec plus d'entraînement.  De là, il a été recruté par SEED Academy, qui est : la plus grande académie de basket-ball d'Afrique de l'Ouest détenue par Amadou Fall.  En 2006-2008, il a participé à la Coupe d'Afrique U19, en Guinée Conakry.  Pape a remporté la coupe des champions 2009 au Sénégal avec US Rail, pour la première fois.  Après cela, il a participé au BWB (Basketball Sans Frontières), a rejoint la Coupe d'Afrique de l'équipe nationale U18 en Guinée Conakry puis l'équipe U20 Nike global challenge à Portland.  En 2013, Pape a obtenu une bourse complète pour venir aux États-Unis et poursuivre ses rêves.  En 2020, il a participé au BAL (Basketball Africa League) Combine au centre de formation Brooklyn Nets HSS.  À partir de là, il a travaillé avec différentes équipes de basket-ball d'État en tant qu'entraîneur, entraîneur et éducateur.  Pape est également un mentor et travaille au sein de sa communauté pour aider à tout événement de sensibilisation, humanitaire ou caritatif.  Il a travaillé pour la magie d'Austin et l'Iowa Swarm ainsi que la mosquée des villes de Quad en tant que joueur de basket-ball.</p>
